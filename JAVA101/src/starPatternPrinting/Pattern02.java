package starPatternPrinting;

public class Pattern02 {

	public static void main(String[] args) {


//		역직각삼각형 (Inverted Right-Angled Triangle)
//		*****
//		****
//		***
//		**
//		*

		
		for(int i = 5; i > 0; i-- ) {
			
			for(int j = i; j > 0; j--) {
				
				System.out.print("*");
				
				
			}
			
			System.out.println();
			
		}
		
		
		
		


	}

}
